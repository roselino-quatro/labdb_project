CREATE TABLE SOLICITACAO_CADASTRO (
    ID_SOLICITACAO INT GENERATED BY DEFAULT AS IDENTITY,
    CPF_PESSOA VARCHAR(11) NOT NULL,
    NUSP VARCHAR(10) NOT NULL,
    STATUS VARCHAR(20) NOT NULL DEFAULT 'PENDENTE',
    CPF_ADMIN_APROVADOR VARCHAR(11),
    DATA_SOLICITACAO TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    DATA_APROVACAO TIMESTAMP,
    OBSERVACOES TEXT,

    CONSTRAINT PK_SOLICITACAO_CADASTRO PRIMARY KEY (ID_SOLICITACAO),
    CONSTRAINT FK_SOLICITACAO_CADASTRO_PESSOA FOREIGN KEY (CPF_PESSOA)
        REFERENCES PESSOA (CPF)
            ON DELETE CASCADE
            ON UPDATE CASCADE,
    CONSTRAINT FK_SOLICITACAO_CADASTRO_ADMIN FOREIGN KEY (CPF_ADMIN_APROVADOR)
        REFERENCES PESSOA (CPF)
            ON DELETE SET NULL
            ON UPDATE CASCADE,
    CONSTRAINT CK_SOLICITACAO_CADASTRO_STATUS CHECK (
        STATUS IN ('PENDENTE', 'APROVADA', 'REJEITADA')
    )
);
